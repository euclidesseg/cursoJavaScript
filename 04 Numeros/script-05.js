// En este capitulo aprenderemos a incrementar o decrementar un numero

let puntaje = 5;
puntaje++; // retorna 5 desde el inicio
++puntaje; //retornara 6 desde el inicido

// El comportamiento es igual para valores negativos
// Aplicandolo desde la consola

// Para incrementar en valores mayorrees se aplica la siguiente sintaxis
puntaje += 5;
puntaje -= 3;
console.log(`${puntaje}`);