// Aunque en ralidad son practicamente lo mismo su unica diferencia tiene que ver en el contexto en el que son utilizadas
const numero1 = 20;
const numero2 = `20`

console.log(parseInt(numero2));// esta es una funcion 
console.log(numero1.toString());// este es un metodo ya que va seguido de un punto
